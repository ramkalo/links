  /* ------------------------------------------------------------- rendering */

  function buildPage(cell) {
    var content = cellContent(cell, links);
    var page = document.createElement('article');
    page.className = 'lab-page';

    /* A tunnel gives you no say in where you go, so it gets no arrows. */
    var arrows = isTunnel(cell) ? '' : DIR_NAMES.filter(function (dir) {
      return !!index.resolveExit(cell, dir);
    }).map(function (dir) {
      return '<button class="lab-arrow lab-arrow-' + dir + '" data-dir="' + dir +
        '" aria-label="go ' + dir + '"></button>';
    }).join('');

    if (isTunnel(cell)) {
      var color = tunnelSetting(cell, 'color');
      page.classList.add('lab-tunnel');
      page.style.background = color;
      if (isLight(color)) page.classList.add('is-light');
    }

    var inner =
      '<span class="lab-title">' + escapeHtml(content.title) + '</span>' +
      (content.description ? '<span class="lab-desc">' + escapeHtml(content.description) + '</span>' : '');

    var body = content.url
      ? '<a class="lab-link" href="' + escapeHtml(content.url) + '" target="_blank" rel="noopener noreferrer">' + inner + '</a>'
      : '<div class="lab-link lab-text">' + inner + '</div>';

    page.innerHTML =
      '<div class="lab-page-inner">' + body +
        (content.note ? '<p class="lab-note">' + escapeHtml(content.note) + '</p>' : '') +
      '</div>' + arrows;

    return page;
  }

  var ENTER = { up: 'translateY(-100%)', down: 'translateY(100%)', left: 'translateX(-100%)', right: 'translateX(100%)' };
  var LEAVE = { up: 'translateY(100%)', down: 'translateY(-100%)', left: 'translateX(100%)', right: 'translateX(-100%)' };

  function showCell(cell, dir) {
    var old = stage.querySelector('.lab-page');
    var page = buildPage(cell);

    current = cell;
    remember(cell);

    if (!old) {
      stage.appendChild(page);
      return;
    }

    moving = true;
    page.style.transform = ENTER[dir];
    stage.appendChild(page);

    /* two frames: one to get the start transform committed, one to animate off it */
    requestAnimationFrame(function () {
      requestAnimationFrame(function () {
        old.classList.add('is-leaving');
        old.style.transform = LEAVE[dir];
        page.style.transform = '';
      });
    });

    setTimeout(function () {
      old.remove();
      moving = false;
    }, 360);
  }

  /* ------------------------------------------------------ tunnel transitions */

  /* Swaps in `page` using one of the five effects, then calls done().
     fade and iris drive the veil; the rest animate the pages themselves. */
  function present(page, effect, dir, ms, done) {
    var old = stage.querySelector('.lab-page');
    var half = Math.max(1, Math.round(ms / 2));

    function land() {
      if (old) old.remove();
      done();
    }

    if (effect === 'fade' || effect === 'iris') {
      veil.className = 'lab-veil is-' + effect;
      veil.style.transitionDuration = half + 'ms';

      requestAnimationFrame(function () {
        veil.classList.add('is-closed');

        setTimeout(function () {
          if (old) old.remove();
          stage.appendChild(page);
          veil.classList.remove('is-closed');
          setTimeout(function () {
            veil.className = 'lab-veil';
            done();
          }, half);
        }, half);
      });
      return;
    }

    if (effect === 'slide') {
      page.style.transform = ENTER[dir] || ENTER.down;
      page.style.transition = 'transform ' + ms + 'ms ease';
      stage.appendChild(page);

      requestAnimationFrame(function () {
        requestAnimationFrame(function () {
          if (old) {
            old.style.transition = 'transform ' + ms + 'ms ease';
            old.style.transform = LEAVE[dir] || LEAVE.down;
          }
          page.style.transform = '';
        });
      });
      setTimeout(land, ms);
      return;
    }

    if (effect === 'flicker') {
      page.style.animation = 'lab-flicker ' + ms + 'ms steps(1, end)';
      stage.appendChild(page);
      setTimeout(function () {
        page.style.animation = '';
        land();
      }, ms);
      return;
    }

    /* crossfade, and the fallback for anything unrecognised */
    page.style.opacity = '0';
    page.style.transition = 'opacity ' + ms + 'ms ease';
    stage.appendChild(page);

    requestAnimationFrame(function () {
      requestAnimationFrame(function () { page.style.opacity = '1'; });
    });
    setTimeout(function () {
      page.style.transition = '';
      page.style.opacity = '';
      land();
    }, ms);
  }

  /* Two independent settings. `fx` is how long a transition takes, played once
     on the way in and once on the way out; `ms` is how long the tunnel sits
     there in between. Door to door a tunnel costs fx + ms + fx. */
  function tunnelTiming(cell) {
    return {
      edge: Math.max(0, Number(tunnelSetting(cell, 'fx')) || 0),
      hold: Math.max(0, Number(tunnelSetting(cell, 'ms')) || 0)
    };
  }

  /* Walks the route a tunnel leads to, playing each one in turn. Entering a
     tunnel uses that tunnel's effect; the last one owns the hand-off into the
     chamber on the far side. */
  function playTunnel(tunnel, dir) {
    var route = tunnelRoute(index, tunnel, dir, Math.random);
    var dest = route.dest || current; /* nothing but tunnels — put them back */

    moving = true;

    function finish() {
      current = dest;
      remember(dest);
      moving = false;
    }

    /* Tunnel i is on screen. Hold it, then transition out into whatever is next
       — which is what puts tunnel i+1 on screen, so that one goes straight to
       its own hold rather than being presented twice. */
    function hold(i) {
      var cell = route.path[i];
      var timing = tunnelTiming(cell);
      var effect = tunnelSetting(cell, 'effect');
      var last = i === route.path.length - 1;
      var onward = route.dirs[i] || dir;

      setTimeout(function () {
        present(buildPage(last ? dest : route.path[i + 1]), effect, onward, timing.edge, function () {
          if (last) finish();
          else hold(i + 1);
        });
      }, timing.hold);
    }

    if (!route.path.length) {
      finish();
      return;
    }

    var first = route.path[0];
    present(buildPage(first), tunnelSetting(first, 'effect'), dir, tunnelTiming(first).edge, function () {
      hold(0);
    });
  }

  function remember(cell) {
    if (previewing) return; /* a test run shouldn't move the real visitor's bookmark */
    try { sessionStorage.setItem(STORAGE_KEY, cell.id); } catch (err) { /* private mode */ }
  }

  function travelTo(next, dir) {
    if (isTunnel(next)) playTunnel(next, dir);
    else showCell(next, dir);
  }

  function move(dir) {
    if (!active || moving) return;

    var next = index.resolveExit(current, dir);
    if (!next) {
      bounce(dir);
      return;
    }
    travelTo(next, dir);
  }

  function bounce(dir) {
    var page = stage.querySelector('.lab-page');
    if (!page) return;

    page.classList.remove('is-bouncing-up', 'is-bouncing-down', 'is-bouncing-left', 'is-bouncing-right');
    void page.offsetWidth; /* restart the animation */
    page.classList.add('is-bouncing-' + dir);
  }

