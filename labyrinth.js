  function buildInventory() {
    var slots = ITEMS.map(function (item, i) {
      return '<li><button class="lab-slot" data-item="' + i + '">' +
        '<span class="lab-slot-icon">' + item.icon + '</span>' +
        '<span class="lab-slot-name">' + escapeHtml(item.name) + '</span>' +
        '</button></li>';
    }).join('');

    inventory.innerHTML =
      '<h2 class="lab-inv-title">inventory</h2>' +
      '<ul class="lab-slots">' + slots + '</ul>' +
      '<p class="lab-flavor" id="lab-flavor"></p>';

    flavorLine = inventory.querySelector('#lab-flavor');
  }

  function toggleInventory(open) {
    var show = open === undefined ? inventory.hidden : open;
    inventory.hidden = !show;
    pack.setAttribute('aria-expanded', String(show));
    if (!show && flavorLine) flavorLine.textContent = '';
  }

  function useItem(i) {
    var item = ITEMS[i];
    if (!item) return;

    if (item.escape) {
      exitLabyrinth();
      return;
    }
    flavorLine.textContent = item.flavor;
  }

  /* ------------------------------------------------------------- enter/exit */

  function enterLabyrinth(cellId) {
    if (active) return;

    var start = index.byId.get(cellId) || index.byId.get(index.maze.start) || index.maze.cells[0];
    if (!start) return;

    /* read the links now rather than at startup: script.js and this file are both
       injected dynamically, so `data` may not have existed yet when init() ran */
    links = buildLinkIndex();

    active = true;
    root.hidden = false;
    document.body.classList.add('labyrinth-active');
    stage.innerHTML = '';

    /* Never open on a tunnel — a stale saved block or a start block someone made
       a tunnel would leave you looking at a transition with nowhere to go. With
       no entry direction nothing is excluded from the pick. */
    if (isTunnel(start)) {
      var route = tunnelRoute(index, start, null, Math.random);
      start = route.dest || index.maze.cells.filter(function (c) { return !isTunnel(c); })[0] || start;
    }

    showCell(start);
    toggleInventory(false);

    if (!previewing && location.hash !== '#labyrinth') location.hash = 'labyrinth';
  }

  /* Runs an arbitrary map in place, for the editor's test button. Nothing is
     written to the address bar or to sessionStorage, so a test run leaves no
     trace on the real site. */
  function preview(maze, startId, onExit) {
    if (!root) return false;

    if (active) exitLabyrinth();

    index = indexMaze(maze);
    previewing = true;
    onPreviewExit = onExit || null;
    enterLabyrinth(startId);
    return true;
  }

  function exitLabyrinth() {
    if (!active) return;

    active = false;
    moving = false;
    root.hidden = true;
    document.body.classList.remove('labyrinth-active');
    stage.innerHTML = '';
    toggleInventory(false);

    if (previewing) {
      var done = onPreviewExit;
      previewing = false;
      onPreviewExit = null;
      if (done) done();
      return;
    }

    /* Leaving forgets where you were, so the next visit starts over. */
    try { sessionStorage.removeItem(STORAGE_KEY); } catch (err) { /* private mode */ }

    if (location.hash === '#labyrinth') {
      history.replaceState(null, '', location.pathname + location.search);
    }
  }

  /* ----------------------------------------------------------------- input */

  function bindGestures() {
    var startX = 0;
    var startY = 0;
    var tracking = false;
    var swiped = false;

    stage.addEventListener('pointerdown', function (e) {
      tracking = true;
      swiped = false;
      startX = e.clientX;
      startY = e.clientY;
    });

    stage.addEventListener('pointerup', function (e) {
      if (!tracking) return;
      tracking = false;

      var dx = e.clientX - startX;
      var dy = e.clientY - startY;

      if (Math.abs(dx) < SWIPE_THRESHOLD && Math.abs(dy) < SWIPE_THRESHOLD) return;
      swiped = true;

      /* Dragging the page up pulls in whatever sits below it, the same way every
         other endless-scroll app works. Same for the other three. */
      if (Math.abs(dy) >= Math.abs(dx)) {
        move(dy < 0 ? 'down' : 'up');
      } else {
        move(dx < 0 ? 'right' : 'left');
      }
    });

    stage.addEventListener('pointercancel', function () { tracking = false; });

    /* a swipe that started on the link shouldn't open the link */
    stage.addEventListener('click', function (e) {
      if (swiped) {
        e.preventDefault();
        swiped = false;
        return;
      }
      var arrow = e.target.closest('.lab-arrow');
      if (arrow) move(arrow.dataset.dir);
    });
  }

  function bindKeys() {
    document.addEventListener('keydown', function (e) {
      if (!active) return;

      if (e.key === 'Escape') {
        toggleInventory(false);
        return;
      }
      var dir = { ArrowUp: 'up', ArrowDown: 'down', ArrowLeft: 'left', ArrowRight: 'right' }[e.key];
      if (!dir) return;

      e.preventDefault();
      move(dir);
    });
  }

  /* ------------------------------------------------------------------ setup */

  function init() {
    root = document.getElementById('labyrinth');
    if (!root) return;

    /* The editor hosts the overlay too, for its test button, and has no toggle. */
    var toggle = document.getElementById('labyrinth-toggle');

    index = indexMaze(loadMaze());

    root.innerHTML =
      '<div class="lab-stage" id="lab-stage"></div>' +
      '<div class="lab-veil" id="lab-veil"></div>' +
      '<button class="lab-pack" id="lab-pack" aria-expanded="false" aria-label="backpack">🎒</button>' +
      '<div class="lab-inventory" id="lab-inventory" hidden></div>';

    stage = root.querySelector('#lab-stage');
    veil = root.querySelector('#lab-veil');
    pack = root.querySelector('#lab-pack');
    inventory = root.querySelector('#lab-inventory');

    buildInventory();
    bindGestures();
    bindKeys();

    if (toggle) {
      /* Going in from the front door always starts at the beginning. Only a
         reload from inside (the #labyrinth case below) picks up where you were. */
      toggle.addEventListener('click', function () { enterLabyrinth(null); });
    }

    pack.addEventListener('click', function () { toggleInventory(); });

    /* the browser's back button should still work — being lost is the joke, being
       trapped isn't */
    window.addEventListener('hashchange', function () {
      if (active && location.hash !== '#labyrinth') exitLabyrinth();
    });

    inventory.addEventListener('click', function (e) {
      var slot = e.target.closest('.lab-slot');
      if (slot) useItem(Number(slot.dataset.item));
    });

    if (location.hash === '#labyrinth') {
      var saved = null;
      try { saved = sessionStorage.getItem(STORAGE_KEY); } catch (err) { /* private mode */ }
      enterLabyrinth(saved);
    }
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }

