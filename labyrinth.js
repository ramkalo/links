  function decodeMaze(blob) {
    var bin = atob(blob);
    var bytes = new Uint8Array(bin.length);
    for (var i = 0; i < bin.length; i++) {
      bytes[i] = bin.charCodeAt(i) ^ KEY.charCodeAt(i % KEY.length);
    }
    return JSON.parse(new TextDecoder().decode(bytes));
  }

  function encodeMaze(maze) {
    var bytes = new TextEncoder().encode(JSON.stringify(maze));
    var bin = '';
    for (var i = 0; i < bytes.length; i++) {
      bin += String.fromCharCode(bytes[i] ^ KEY.charCodeAt(i % KEY.length));
    }
    return btoa(bin);
  }

  function loadMaze() {
    try {
      return decodeMaze(LABYRINTH_CACHE);
    } catch (err) {
      return labyrinth;
    }
  }

  /* ------------------------------------------------------------ maze lookup */

  /* Wraps a maze in id/coordinate lookups plus the one movement rule everything
     else defers to. The editor and _tools/maze-check.js call this too, so the
     three can never disagree about where a swipe leads. */
  function indexMaze(maze) {
    var byId = new Map();
    var byXY = new Map();

    maze.cells.forEach(function (cell) {
      byId.set(cell.id, cell);
      byXY.set(cell.x + ',' + cell.y, cell);
    });

    return {
      maze: maze,
      byId: byId,
      byXY: byXY,

      /* 1. an explicit exit id wins  -> wormhole, can land anywhere
         2. an explicit null          -> hard wall
         3. otherwise                 -> the neighbouring square, if one exists */
      resolveExit: function (cell, dir) {
        if (cell.exits && Object.prototype.hasOwnProperty.call(cell.exits, dir)) {
          var target = cell.exits[dir];
          return target ? byId.get(target) || null : null;
        }
        var step = DIRS[dir];
        return byXY.get((cell.x + step[0]) + ',' + (cell.y + step[1])) || null;
      }
    };
  }

  function reachableFrom(index, startId) {
    var start = index.byId.get(startId) || index.maze.cells[0];
    if (!start) return new Set();

    var seen = new Set([start.id]);
    var queue = [start];

    while (queue.length) {
      var cell = queue.shift();
      DIR_NAMES.forEach(function (dir) {
        var next = index.resolveExit(cell, dir);
        if (next && !seen.has(next.id)) {
          seen.add(next.id);
          queue.push(next);
        }
      });
    }
    return seen;
  }

