  var STORAGE_KEY = 'labyrinth-cell';
  var SWIPE_THRESHOLD = 50;

  var index = null;
  var links = null;
  var current = null;
  var active = false;
  var moving = false;
  var previewing = false;  /* running inside the editor's test button */
  var onPreviewExit = null;

