  function isLight(color) {
    var hex = String(color || '').replace('#', '');
    if (hex.length === 3) hex = hex[0] + hex[0] + hex[1] + hex[1] + hex[2] + hex[2];
    if (!/^[0-9a-f]{6}$/i.test(hex)) return false;

    var r = parseInt(hex.slice(0, 2), 16);
    var g = parseInt(hex.slice(2, 4), 16);
    var b = parseInt(hex.slice(4, 6), 16);
    return (r * 0.299 + g * 0.587 + b * 0.114) > 150;
  }

  function tunnelSetting(cell, key) {
    var conf = cell.tunnel || {};
    return conf[key] === undefined || conf[key] === null ? TUNNEL_DEFAULTS[key] : conf[key];
  }

  /* Weighted pick among the neighbours a tunnel actually has.
     `entryDir` is the direction you were travelling when you arrived, so the way
     back out is the opposite of it — weighted by `back`, which defaults to 0. */
  function pickExit(index, cell, entryDir, rng) {
    var exits = [];
    DIR_NAMES.forEach(function (dir) {
      var next = index.resolveExit(cell, dir);
      if (next) exits.push({ dir: dir, cell: next });
    });
    if (!exits.length) return null;

    var backDir = entryDir ? OPPOSITE[entryDir] : null;
    var weights = (cell.tunnel || {}).weights || {};

    var forward = exits.filter(function (e) { return e.dir !== backDir; });
    var back = exits.filter(function (e) { return e.dir === backDir; })[0];

    var pool = forward.map(function (e) {
      var w = weights[e.dir];
      return { exit: e, weight: typeof w === 'number' && w >= 0 ? w : 1 };
    });
    if (back) pool.push({ exit: back, weight: Math.max(0, tunnelSetting(cell, 'back')) });

    var total = pool.reduce(function (sum, p) { return sum + p.weight; }, 0);

    /* Everything weighted to zero. Split evenly among the ways onward, and only
       hand them back the way they came if there is no other way at all. */
    if (total <= 0) {
      var evens = forward.length ? forward : (back ? [back] : []);
      if (!evens.length) return null;
      return evens[Math.min(evens.length - 1, Math.floor(rng() * evens.length))];
    }

    var roll = rng() * total;
    for (var i = 0; i < pool.length; i++) {
      roll -= pool[i].weight;
      if (roll < 0) return pool[i].exit;
    }
    return pool[pool.length - 1].exit;
  }

  /* Walks a run of tunnels to the chamber on the far side. Safe to call on any
     block: a chamber returns itself with an empty path. `dest` comes back null
     only if the tunnels never reach a chamber, which the caller treats as "stay
     where you were" rather than stranding anyone. */
  function tunnelRoute(index, cell, entryDir, rng) {
    var random = rng || Math.random;
    var path = [];
    var dirs = []; /* dirs[i] is the way you leave path[i] */
    var at = cell;
    var dir = entryDir || null;

    for (var hop = 0; hop < TUNNEL_HOPS && isTunnel(at); hop++) {
      path.push(at);

      var pick = pickExit(index, at, dir, random);
      if (!pick) break;

      dirs.push(pick.dir);
      at = pick.cell;
      dir = pick.dir;
    }

    return { path: path, dirs: dirs, dest: isTunnel(at) ? null : at, dir: dir };
  }

  /* --------------------------------------------------------------- contents */

  /* Cells point at a link by url, so editing a link in script.js updates the
     maze too. A cell can override the title or add a note on top of that. */
  function buildLinkIndex() {
    var links = new Map();
    if (typeof data === 'undefined' || !data.categories) return links;

    data.categories.forEach(function (category) {
      (category.links || []).forEach(function (link) {
        links.set(link.url, link);
      });
    });
    return links;
  }

  /* A cell with no link at all is a text-only page — somewhere to put a line of
     nonsense with nothing to click. */
  function cellContent(cell, links) {
    var link = links.get(cell.link) || {};
    var url = cell.link || link.url || '';
    return {
      url: url,
      /* a tunnel with nothing written on it is just a colored flash, not a "???" */
      title: cell.title || link.title || url || (isTunnel(cell) ? '' : '???'),
      description: cell.description || link.description || '',
      note: cell.note || ''
    };
  }

